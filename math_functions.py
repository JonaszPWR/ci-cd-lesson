"""Own math functions"""


def add(a: int, b: int) -> int:
    """Adder"""
    return a + b


def subtract(a: int, b: int) -> int:
    """Adder but the second number is multiplied by -1"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Product generator"""
    return a * b


def divide(a: int, b: int) -> float:
    """Product generator but the second number is raised to -1"""
    return a / b
